class Calculadora {
    sumar(a,b){
        return a+b;
    }
    restar(a,b){
        return a-b;
    }
    multiplicar(a,b){
        return a*b;
    }
    dividir(a,b){
        return a/b;
    }
    raizCuadrada(a){
        
        return Math.sqrt(a);
    }
    potencia(a,b){
       return Math.pow(a,b);
    }

}